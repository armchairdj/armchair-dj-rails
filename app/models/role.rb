class Role < ApplicationRecord

  #############################################################################
  # CONSTANTS.
  #############################################################################

  #############################################################################
  # CONCERNS.
  #############################################################################

  #############################################################################
  # CLASS.
  #############################################################################

  def self.admin_scopes
    {
      "All" => :all
    }
  end

  #############################################################################
  # SCOPES.
  #############################################################################

  scope :eager,     -> { all }
  scope :for_admin, -> { all }
  scope :for_site,  -> { all }

  #############################################################################
  # ASSOCIATIONS.
  #############################################################################

  belongs_to :medium

  has_many :contributions, dependent: :destroy

  #############################################################################
  # ATTRIBUTES.
  #############################################################################

  #############################################################################
  # VALIDATIONS.
  #############################################################################

  validates :medium, presence: true

  #############################################################################
  # HOOKS.
  #############################################################################

  #############################################################################
  # INSTANCE.
  #############################################################################

end
