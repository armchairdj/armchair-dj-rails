class Role < ApplicationRecord

  #############################################################################
  # CONSTANTS.
  #############################################################################

  #############################################################################
  # CONCERNS.
  #############################################################################

  include Alphabetizable

  #############################################################################
  # CLASS.
  #############################################################################

  def self.admin_scopes
    {
      "All" => :for_admin
    }
  end

  def self.grouped_options
    joins(:medium).alpha.group_by{ |w| w.medium.name }.to_a.sort_by(&:first)
  end

  #############################################################################
  # SCOPES.
  #############################################################################

  scope :eager,     -> { includes(:contributions) }
  scope :for_admin, -> { eager }

  #############################################################################
  # ASSOCIATIONS.
  #############################################################################

  belongs_to :medium

  has_many :contributions, dependent: :destroy

  #############################################################################
  # ATTRIBUTES.
  #############################################################################

  #############################################################################
  # VALIDATIONS.
  #############################################################################

  validates :medium, presence: true
  validates :name,   presence: true

  validates :name, uniqueness: { scope: [:medium_id] }

  #############################################################################
  # HOOKS.
  #############################################################################

  #############################################################################
  # INSTANCE.
  #############################################################################

  def alpha_parts
    [medium.try(:alpha_parts), name]
  end

  def display_name(full: false)
    return self.name unless full

    [self.medium.name, self.name].join(": ")
  end
end
