class Category < ApplicationRecord

  #############################################################################
  # CONSTANTS.
  #############################################################################

  #############################################################################
  # CONCERNS.
  #############################################################################

  include Alphabetizable

  #############################################################################
  # CLASS.
  #############################################################################

  #############################################################################
  # SCOPES.
  #############################################################################

  scope :multi,     -> { where(allow_multiple: true ) }
  scope :single,    -> { where(allow_multiple: false) }

  scope :eager,     -> { includes(:facets, :media, :aspects) }
  scope :for_admin, -> { eager }
  scope :for_site,  -> { eager.alpha }

  #############################################################################
  # ASSOCIATIONS.
  #############################################################################

  has_many :facets, inverse_of: :category, dependent: :destroy

  has_many :media, through: :facets

  has_many :aspects, dependent: :destroy

  #############################################################################
  # ATTRIBUTES.
  #############################################################################

  #############################################################################
  # VALIDATIONS.
  #############################################################################

  validates :name, presence: true

  #############################################################################
  # HOOKS.
  #############################################################################

  #############################################################################
  # INSTANCE.
  #############################################################################

  def display_name
    allow_multiple? ? name.pluralize : name
  end

  def alpha_parts
    [name]
  end

  def multi?
    allow_multiple?
  end

  def single?
    !allow_multiple?
  end
end
