class Milestone < ApplicationRecord

  #############################################################################
  # CONSTANTS.
  #############################################################################

  #############################################################################
  # CONCERNS.
  #############################################################################

  #############################################################################
  # CLASS.
  #############################################################################

  #############################################################################
  # SCOPES.
  #############################################################################

  #############################################################################
  # ASSOCIATIONS.
  #############################################################################

  belongs_to :work

  #############################################################################
  # ATTRIBUTES.
  #############################################################################

  enum action: {
    released:   0,
    reissued:   1,
    remastered: 2,
    created:    3,
  }

  enumable_attributes :action

  #############################################################################
  # VALIDATIONS.
  #############################################################################

  validates :work, presence: true

  validates :action, presence: true

  validates :year, presence: true, yearness: true

  #############################################################################
  # HOOKS.
  #############################################################################

  #############################################################################
  # INSTANCE.
  #############################################################################

end
