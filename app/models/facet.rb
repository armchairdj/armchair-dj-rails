class Facet < ApplicationRecord

  #############################################################################
  # CONSTANTS.
  #############################################################################

  #############################################################################
  # CONCERNS.
  #############################################################################

  #############################################################################
  # CLASS.
  #############################################################################

  #############################################################################
  # SCOPES.
  #############################################################################

  scope :sorted,    -> { joins(:category, :medium).order("media.name, categories.name") }
  scope :eager,     -> { includes(:medium, :category) }
  scope :for_admin, -> { eager.sorted }
  scope :for_site,  -> { eager.sorted }

  #############################################################################
  # ASSOCIATIONS.
  #############################################################################

  belongs_to :medium,   inverse_of: :facets
  belongs_to :category, inverse_of: :facets

  # acts_as_list scope: [:medium_id]

  #############################################################################
  # ATTRIBUTES.
  #############################################################################

  #############################################################################
  # VALIDATIONS.
  #############################################################################

  validates :medium,   presence: true
  validates :category, presence: true

  #############################################################################
  # HOOKS.
  #############################################################################

  #############################################################################
  # INSTANCE.
  #############################################################################

end
