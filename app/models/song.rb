class Song < ApplicationRecord

  #############################################################################
  # CONSTANTS.
  #############################################################################

  #############################################################################
  # CONCERNS & PLUGINS.
  #############################################################################

  include Contributable

  #############################################################################
  # ASSOCIATIONS.
  #############################################################################

  has_many :posts, as: :postable, dependent: :destroy

  #############################################################################
  # ENUMS.
  #############################################################################

  #############################################################################
  # SCOPES.
  #############################################################################

  scope :alphabetical, -> { order(:title) }

  #############################################################################
  # VALIDATIONS.
  #############################################################################

  validates :title, presence: true

  #############################################################################
  # HOOKS.
  #############################################################################

  #############################################################################
  # INSTANCE.
  #############################################################################

  #############################################################################
  # CLASS.
  #############################################################################

end
